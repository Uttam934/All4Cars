package com.allforcars.all4cars.Model;

public class bonus_Setting_Model {

    private String bonus_id;
    private String bonus_point;
    private String amount;
    private String status;




    public String getbonus_point() {
        return bonus_point;
    }

    public void setbonus_point(String bonus_point) {
        this.bonus_point = bonus_point;
    }


    public String getbonus_id() {
        return bonus_id;
    }

    public void setbonus_id(String bonus_id) {
        this.bonus_id = bonus_id;
    }



    public String getamount() {
        return amount;
    }

    public void setamount(String amount) {
        this.amount = amount;
    }

    public String getstatus() {
        return status;
    }

    public void setstatus(String status) {
        this.status = status;
    }




}
